             <br>      
            <span>在近日结束的进博会上，欧莱雅中国总裁兼首席执行官费博瑞向媒体感叹，中国市场变化越来越快，企业必须加速转型才能适应消费者需求变化趋势。事实上，目前中国美妆市场高度同质化竞争已经让越来越多的品牌意识到，必须走出差异化路线才能获得市场。在这一趋势下，越来越多的新品以及整合全球供应链的“全球直造”等模式开始涌现。</span> 
