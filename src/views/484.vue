            <span>提及中国美妆市场，国际商务数字化营销专家Kenx Gao认为，中国市场庞大，呈现消费升级与消费降级两大趋势并存的格局。“回顾这十年间中国消费市场最大的变化，就是消费升级。而背后，驱动其不断变化的因素有二，一方面是高收入人群的崛起。我们把家庭月可支配收入超过12000元定义为上层中产，在2016年，中国上层中产的人数约为5000万人，差不多与一个欧洲国家的人口数持平，而到2020年，这部分人口的数量预计将达到一亿人，相比2016年翻一倍。当经济条件更为宽裕后，人们对于商品的品质要求的提高成为刺激消费升级的第一个驱动力。第二个驱动力我认为是来自于消费主力的转换。分享一组数据：2018年，整个80、90后消费增长占整体社会消费品增长的70%，这意味着80、90后新世代变为了消费的主力，而这一代人，生活在物质丰富的时代环境下，从小就受到国际品牌的影响，相较于以往传统的消费主力，对产品的需求更精明，更挑剔。因而，在面临当下消费者经济水平提升，带动对产品品质的要求拔高的大趋势背景下，‘全球直造’等模式对行行业发展很有意义。”Kenx Gao表示，
